#!/usr/bin/env node
// CF7:ME 存档骨架抽取工具
// 输入：projectRoot/saves/crazyflasher7_saves.json（开发者自己的健康档）
// 输出：launcher/data/save_schema.json（作为"出厂默认参考"，用于 diff 视图与未来 schema 扩展）
//
// 设计：
//   - 完整保留档结构（不裁剪），让所有字段都能被 diff 视图覆盖
//   - 数值字段保留实际值（≈"出厂值参考"，而非通用 0）；这是用户在 Phase 5.8 设计中明确选择的口径：
//     "权威源是否本身也可以作为一个存档或者配置文档存在？当前可从我的存档里面获取"
//   - 抽取时把"明确属于角色个性化"的字段替换为通用初值（防止把开发者档里的等级/经验泄漏成默认值）
//     参考 archive-schema.js 中已经标注 default 的字段；这些字段被替换为对应 default
//   - schema 中有 default 的字段以 schema 为准；其它字段保留档原值
//
// 用法：
//   node tools/extract-save-schema.js [--source <path>] [--out <path>]
//   默认 source = saves/crazyflasher7_saves.json
//   默认 out    = launcher/data/save_schema.json

'use strict';

var fs = require('fs');
var path = require('path');

function parseArgs(argv) {
  var args = { source: null, out: null };
  for (var i = 2; i < argv.length; i++) {
    if (argv[i] === '--source' && i + 1 < argv.length) { args.source = argv[++i]; }
    else if (argv[i] === '--out' && i + 1 < argv.length) { args.out = argv[++i]; }
    else if (argv[i] === '--help' || argv[i] === '-h') { args.help = true; }
  }
  return args;
}

function printHelp() {
  console.log('用法: node tools/extract-save-schema.js [--source <path>] [--out <path>]');
  console.log('  --source  输入档（默认 saves/crazyflasher7_saves.json）');
  console.log('  --out     输出骨架（默认 launcher/data/save_schema.json）');
}

// 与 archive-schema.js 共享的"明确字段默认值"白名单。
// 这些字段在生成骨架时会被替换为下方的 default，避免把开发者档里的进度/等级当成默认。
// path: 数组形式（与 archive-schema.js 中 path 一致）
// 注：schema 字段值可能是字符串或数字，按其类型替换。
var SCHEMA_DEFAULTS = [
  // 角色基本属性（与 archive-schema.js 同步）
  { path: ['0', 2], value: 0 },        // 金钱
  { path: ['0', 3], value: 1 },        // 等级
  { path: ['0', 4], value: 0 },        // 经验
  { path: ['0', 6], value: 0 },        // 技能点
  { path: ['0', 8], value: 0 },        // 身价
  { path: ['0', 9], value: 0 },        // 虚拟币
  // 主线进度
  { path: ['3'], value: 0 },
  // 健身五维
  { path: ['7', 0], value: 0 },
  { path: ['7', 1], value: 0 },
  { path: ['7', 2], value: 0 },
  { path: ['7', 3], value: 0 },
  { path: ['7', 4], value: 0 },
  // 系统设置
  { path: ['others', '设置', 'setGlobalVolume'], value: 50 },
  { path: ['others', '设置', 'setBGMVolume'], value: 80 }
];

function setByPath(obj, p, value) {
  var cur = obj;
  for (var i = 0; i < p.length - 1; i++) {
    if (cur == null) return;  // 路径不存在，跳过（保留档原值）
    var k = p[i];
    if (cur[k] == null) return;
    cur = cur[k];
  }
  if (cur != null && cur[p[p.length - 1]] !== undefined) {
    cur[p[p.length - 1]] = value;
  }
}

function deepClone(o) {
  return JSON.parse(JSON.stringify(o));
}

function main() {
  var args = parseArgs(process.argv);
  if (args.help) { printHelp(); return 0; }

  var projectRoot = path.resolve(__dirname, '..');
  var sourcePath = args.source ? path.resolve(args.source) : path.join(projectRoot, 'saves', 'crazyflasher7_saves.json');
  var outPath = args.out ? path.resolve(args.out) : path.join(projectRoot, 'launcher', 'data', 'save_schema.json');

  console.log('source: ' + sourcePath);
  console.log('out:    ' + outPath);

  if (!fs.existsSync(sourcePath)) {
    console.error('[FAIL] 源档不存在: ' + sourcePath);
    return 1;
  }

  var raw;
  try { raw = fs.readFileSync(sourcePath, 'utf8'); }
  catch (e) { console.error('[FAIL] 读取源档失败: ' + e.message); return 1; }

  var data;
  try { data = JSON.parse(raw); }
  catch (e) { console.error('[FAIL] 源档不是合法 JSON: ' + e.message); return 1; }

  // 1. 完整克隆
  var skeleton = deepClone(data);

  // 2. 套用 schema 默认值
  var applied = 0;
  for (var i = 0; i < SCHEMA_DEFAULTS.length; i++) {
    var d = SCHEMA_DEFAULTS[i];
    setByPath(skeleton, d.path, d.value);
    applied++;
  }

  // 3. 加注元信息（不会进入档逻辑，仅人类可读）
  // 注：放在 __meta 字段而不是顶层 _meta，避免与 lastSaved 之类同层混淆；
  // diff 视图基于 path 比较，__meta 路径在 archive-schema.js 中没有任何字段引用，自然不会被显示
  skeleton.__meta = {
    note: 'Auto-generated by tools/extract-save-schema.js. Do NOT edit by hand.',
    sourcedFrom: path.basename(sourcePath),
    generatedAt: new Date().toISOString(),
    schemaDefaultsApplied: applied
  };

  // 4. 写出
  var outDir = path.dirname(outPath);
  if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });
  try {
    fs.writeFileSync(outPath, JSON.stringify(skeleton, null, 2), 'utf8');
  } catch (e) {
    console.error('[FAIL] 写出失败: ' + e.message);
    return 1;
  }

  console.log('[OK] 骨架已生成 (' + applied + ' 个 schema 默认值套用，' +
    (raw.length / 1024).toFixed(1) + ' KB → ' + (fs.statSync(outPath).size / 1024).toFixed(1) + ' KB)');
  return 0;
}

process.exit(main());
